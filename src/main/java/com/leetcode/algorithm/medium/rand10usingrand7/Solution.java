package com.leetcode.algorithm.medium.rand10usingrand7;

import com.leetcode.algorithm.common.SolBase;

class Solution extends SolBase {
  public int rand10() {
    int row, col, idx;

    do {
      row = rand7();
      col = rand7();
      idx = col + (row - 1) * 7;
    } while (idx > 40);

    return 1 + (idx - 1) % 10;
  }
}
